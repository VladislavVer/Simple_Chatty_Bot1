import java.util.Scanner

fun main(args: Array<String>) {
    val scanner: Scanner = Scanner(System.`in`)
    val str1 = scanner.nextLine()
    val str2 = scanner.nextLine()
    val int1 = scanner.nextInt()
    val int2 = scanner.nextInt()

    println(str1)
    println(str2)
    println(int1)
    println(int2)
}